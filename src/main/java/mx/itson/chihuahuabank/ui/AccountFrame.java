    private void btnFileActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnFileActionPerformed
            FileDialog fileDialog = new FileDialog(this, "Select File", FileDialog.LOAD);
                fileDialog.setVisible(true);

                String directory = fileDialog.getDirectory();
                String filename = fileDialog.getFile();

                if (filename != null) {
                    File selectedFile = new File(directory, filename);
                    try (BufferedReader reader = new BufferedReader(new FileReader(selectedFile))) {
                        StringBuilder content = new StringBuilder();
                        String line;
                        while ((line = reader.readLine()) != null) {
                            content.append(line).append("\n");
                        }
                        String contentt = content.toString();  
                        
                        Account a = Account.deserialize(contentt);
                        
                        lblTest.setText(a.getAccountNumber());
                        lblNameHolder.setText(a.getAccountHolder().getName());
                        
                    } catch (IOException ex) {
                        ex.printStackTrace();
                    }
                }
        
    }//GEN-LAST:event_btnFileActionPerformed

